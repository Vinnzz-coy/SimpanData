<?php

namespace Database\Seeders;

use Illuminate\Database\Seeder;
use Illuminate\Support\Facades\Hash;
use App\Models\User;
use App\Models\Peserta;
use App\Models\Absensi;
use Carbon\Carbon;
use Carbon\CarbonPeriod;

class IpinSeeder extends Seeder
{
    /**
     * Run the database seeds.
     */
    public function run(): void
    {
        $user = User::firstOrCreate(
            ['email' => 'ipinipin@gmail.com'],
            [
                'username' => 'ipin',
                'password' => Hash::make('ipin 123'),
                'role' => 'peserta',
            ]
        );

        $peserta = Peserta::firstOrCreate(
            ['user_id' => $user->id],
            [
                'nama' => 'Ipin',
                'asal_sekolah_universitas' => 'Universitas Durian Runtuh',
                'jurusan' => 'Teknik Goreng Ayam',
                'alamat' => 'Kampung Durian Runtuh',
                'no_telepon' => '081234567899',
                'jenis_kegiatan' => 'Magang', // or PKL
                'tanggal_mulai' => Carbon::now()->subMonths(6)->toDateString(),
                'tanggal_selesai' => Carbon::now()->addMonths(6)->toDateString(),
                'status' => 'Aktif',
            ]
        );

        // 3. Generate Absensi for the last 3 months
        $startDate = Carbon::now()->subMonths(3);
        $endDate = Carbon::now();

        $period = CarbonPeriod::create($startDate, $endDate);

        foreach ($period as $date) {
            // Skip weekends (Saturday and Sunday)
            if ($date->isWeekend()) {
                continue;
            }

            // Randomize times slightly for realism
            // Masuk: 07:30 - 08:30
            $jamMasuk = $date->copy()->setTime(rand(7, 8), rand(0, 59), rand(0, 59));
            if ($jamMasuk->hour == 7 && $jamMasuk->minute < 30) {
                $jamMasuk->addMinutes(30); // Ensure after 7:30
            }

            // Pulang: 16:30 - 18:00
            $jamPulang = $date->copy()->setTime(rand(16, 17), rand(0, 59), rand(0, 59));
             if ($jamPulang->hour == 16 && $jamPulang->minute < 30) {
                $jamPulang->addMinutes(30); // Ensure after 16:30
            }

            // Determine Mode Kerja (mostly WFO, occasionally WFA)
            $modeKerja = (rand(1, 10) > 2) ? 'WFO' : 'WFA';

            // Create 'Masuk' record
            Absensi::firstOrCreate(
                [
                    'peserta_id' => $peserta->id,
                    'jenis_absen' => 'Masuk',
                    'waktu_absen' => $jamMasuk,
                ],
                [
                    'mode_kerja' => $modeKerja,
                    'status' => 'Hadir', // Assuming 'Hadir' is valid enum
                    'wa_pengirim' => $peserta->no_telepon,
                ]
            );

            // Create 'Pulang' record
            Absensi::firstOrCreate(
                [
                    'peserta_id' => $peserta->id,
                    'jenis_absen' => 'Pulang',
                    'waktu_absen' => $jamPulang,
                ],
                [
                    'mode_kerja' => $modeKerja,
                    'status' => 'Hadir',
                    'wa_pengirim' => $peserta->no_telepon,
                ]
            );
        }
    }
}
